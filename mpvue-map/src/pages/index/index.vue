<template>
  <div class="container" @click="clickHandle('test click', $event)">

    <button @click="onGoBubblesClick('test click', $event)">移动气泡</button>
    <button @click="onGoWebViewTestClick('test click', $event)">WebView组件</button>

  </div>
</template>

<script>
import card from "@/components/card";

export default {
  data() {
    return {
      motto: "Hello World",
      userInfo: {}
    };
  },

  components: {
    card
  },

  methods: {
    getUserInfo() {
      // 调用登录接口
      wx.login({
        success: () => {
          wx.getUserInfo({
            success: res => {
              this.userInfo = res.userInfo;
            }
          });
        }
      });
    },
    clickHandle(msg, ev) {
      console.log("clickHandle:", msg, ev);
    },
    onGoBubblesClick(ev) {
      const url = "/pages/movingBubble/movingBubble";
      let arg = { url };
      wx.navigateTo(arg);
    },
    onGoWebViewTestClick(ev) {
      const url = "/pages/webViewTest/webViewTest";
      let arg = { url };
      wx.navigateTo(arg);
    }
  },

  created() {
    // 调用应用实例的方法获取全局数据
    this.getUserInfo();
  }
};
</script>

